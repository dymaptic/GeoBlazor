                                                                              {
                                                                                  return default!;
                                                                              }
                                                                              return (T?)(object?){{variableName
                                                                              }}
                                                              ?.Items?.Select(i => i.FromSerializationRecord()).Cast<{{
                                                                  protoSerializableType}}>().ToArray();
                                                              """);

            if (definition.Name == "Attribute")
            {
                // Attribute is not a GeoBlazor type so skip next injection
                continue;
            }

            protoMemberGenerationMethod.AppendLine($$"""
                                                                 case "{{protoSerializableType}}":
                                                                     {{definition.Name}}SerializationRecord {{
                                                                         variableName}} = 
                                                                         (({{protoSerializableType
                                                                         }})value).ToProtobuf();
                                                                     Serializer.Serialize(memoryStream, {{variableName
                                                                     }});
                                                                     
                                                                     break;
                                                     """);

            protoCollectionGenerationMethod.AppendLine($$"""
                                                                     case "{{protoSerializableType}}":
                                                                         {{definition.Name
                                                                         }}CollectionSerializationRecord {{variableName
                                                                         }} = 
                                                                             new(items.Cast<{{protoSerializableType
                                                                             }}
                                                         >().Select(i => i.ToProtobuf()).ToArray());
                                                                         Serializer.Serialize(memoryStream, {{
                                                                             variableName}});
                                                                         
                                                                         break;
                                                         """);
        }

        protoTypeDictionary.AppendLine("    };");
        protoCollectionTypeDictionary.AppendLine("    };");

        readJsProtoStreamRefMethod.AppendLine("""
                                                      }
                                                      
                                                      return default!;
                                                  }
                                              """);

        readJsProtoCollectionStreamRefMethod.AppendLine("""
                                                                }
                                                                
                                                                return default!;
                                                            }
                                                        """);

        protoMemberGenerationMethod.AppendLine("""
                                                       }
                                                       
                                                       memoryStream.Seek(0, SeekOrigin.Begin);

                                                       if (isServer)
                                                       {
                                                           return new DotNetStreamReference(memoryStream);
                                                       }
                                                       
                                                       byte[] data = memoryStream.ToArray();
                                                       memoryStream.Dispose();
                                                       
                                                       return data;
                                                   }
                                               """);

        protoCollectionGenerationMethod.AppendLine("""
                                                           }
                                                           
                                                           memoryStream.Seek(0, SeekOrigin.Begin);

                                                           if (isServer)
                                                           {
                                                               return new DotNetStreamReference(memoryStream);
                                                           }
                                                           
                                                           byte[] data = memoryStream.ToArray();
                                                           memoryStream.Dispose();
                                                           
                                                           return data;
                                                       }
                                                   """);

        return $"""
                {readJsProtoStreamRefMethod}

                {readJsProtoCollectionStreamRefMethod}

                {protoMemberGenerationMethod}

                {protoCollectionGenerationMethod}

                {protoTypeDictionary}

                {protoCollectionTypeDictionary}
                """;
    }

    private static string GenerateSerializableMethodRecords(
        ImmutableArray<SerializableMethodRecord> serializedMethodsCollection, bool isPro)
    {
        StringBuilder outputBuilder;

        if (isPro)
        {
            outputBuilder = new("""
                                    /// <summary>
                                    ///     A collection of serializable methods and their parameters/return types.
                                    /// </summary>
                                    public static partial Dictionary<string, SerializableMethodRecord[]> SerializableMethods => _serializableMethods
                                        .Concat(CoreSerializationData.SerializableMethods)
                                        .ToDictionary(kvp => kvp.Key, kvp => kvp.Value);

                                    private static readonly Dictionary<string, SerializableMethodRecord[]> _serializableMethods = new()
                                    {

                                """);
        }
        else
        {
            outputBuilder = new("""
                                    /// <summary>
                                    ///     A collection of serializable methods and their parameters/return types.
                                    /// </summary>
                                    public static partial Dictionary<string, SerializableMethodRecord[]> SerializableMethods => _serializableMethods;

                                    private static readonly Dictionary<string, SerializableMethodRecord[]> _serializableMethods = new()
                                    {

                                """);
        }

        List<IGrouping<string, SerializableMethodRecord>> classGroups = serializedMethodsCollection
            .GroupBy(m => m.ClassName)
            .ToList();

        for (int i = 0; i < classGroups.Count; i++)
        {
            IGrouping<string, SerializableMethodRecord> classGroup = classGroups[i];

            outputBuilder.AppendLine($$"""
                                               ["{{classGroup.Key}}"] = 
                                                   [
                                       """);

            List<SerializableMethodRecord> methodRecords = classGroup.ToList();

            for (int j = 0; j < methodRecords.Count; j++)
            {
                SerializableMethodRecord methodRecord = methodRecords[j];

                if (methodRecord.Parameters.Values.Contains("T")
                    || methodRecord.Parameters.Values.Contains("T?")
                    || (methodRecord.ReturnType == "T")
                    || (methodRecord.ReturnType == "T?")
                    || (methodRecord.ReturnType == "T[]")
                    || (methodRecord.ReturnType == "T[]?")
                    || (methodRecord.ReturnType?.Contains("<T>") == true))
                {
                    continue;
                }

                outputBuilder.AppendLine($$"""
                                                           new SerializableMethodRecord("{{
                                                               methodRecord.MethodName.ToLowerFirstChar()}}",
                                                               [
                                           """);

                List<string> parameters = methodRecord.Parameters.Values.ToList();

                for (int k = 0; k < parameters.Count; k++)
                {
                    string param = parameters[k];
                    outputBuilder.Append(GenerateSerializableParameterRecord(param, 24));

                    if (k < parameters.Count - 1)
                    {
                        outputBuilder.AppendLine(",");
                    }
                    else
                    {
                        outputBuilder.AppendLine();
                    }
                }

                if (methodRecord.ReturnType != null)
                {
                    outputBuilder.AppendLine("                    ],");
                    outputBuilder.Append(GenerateSerializableParameterRecord(methodRecord.ReturnType, 20));

                    if (j < methodRecords.Count - 1)
                    {
                        outputBuilder.AppendLine("),");
                    }
                    else
                    {
                        outputBuilder.AppendLine(")");
                    }
                }
                else
                {
                    if (j < methodRecords.Count - 1)
                    {
                        outputBuilder.AppendLine("                ]),");
                    }
                    else
                    {
                        outputBuilder.AppendLine("                ]");
                    }
                }
            }

            if (i < classGroups.Count - 1)
            {
                outputBuilder.AppendLine("       ],");
            }
            else
            {
                outputBuilder.AppendLine("       ]");
            }
        }

        outputBuilder.AppendLine("    };");

        return outputBuilder.ToString();
    }

    private static string GenerateSerializableParameterRecord(string value, int indent)
    {
        bool isNullable = value.EndsWith("?");
        string trimmedValue = value.Replace("?", ""); // replace all ?, even in the inner single type
        string isNullableText = isNullable ? "true" : "false";
        string? singleType = null;

        if (trimmedValue.EndsWith("[]")) // check against the trimmed version without ?
        {
            // use untrimmed value again to restore ? in the single types
            int arrayStart = value.IndexOf("[", StringComparison.OrdinalIgnoreCase);
            singleType = value.Substring(0, arrayStart);
        }
        else if (value.Contains("<") && trimmedValue.Contains(">"))
        {
            // use param.Value again to restore ? in the single types
            int genericStart = value.IndexOf("<", StringComparison.OrdinalIgnoreCase);
            singleType = value.Substring(genericStart + 1, trimmedValue.Length - genericStart - 2);
        }

        bool singleTypeIsNullable = singleType?.EndsWith("?") == true;
        string singleTypeIsNullableText = singleTypeIsNullable ? "true" : "false";
        singleType = singleType?.Replace("?", "");

        string collectionText = singleType is null
            ? "null"
            : $"typeof({singleType})";

        string padding = new string(' ', indent);

        return $"""
                {padding}new SerializableParameterRecord(typeof({trimmedValue}), {isNullableText}, 
                {padding}    {collectionText}, {singleTypeIsNullableText})
                """;
    }

    private static List<SerializableMethodRecord> ToSerializableMethodRecords(this BaseTypeDeclarationSyntax typeSyntax)
    {
        List<MethodDeclarationSyntax> methods = typeSyntax
            .ChildNodes()
            .OfType<MethodDeclarationSyntax>()
            .Where(m => m.AttributeLists
                .SelectMany(a => a.Attributes)
                .Any(attr => attr.Name.ToString() == SerializedMethodAttributeName))
            .ToList();

        List<SerializableMethodRecord> methodRecords = [];

        foreach (MethodDeclarationSyntax method in methods)
        {
            string returnType = method.ReturnType.ToString();

            if (returnType.StartsWith("Task") || returnType.StartsWith("ValueTask"))
            {
                int bracketIndex = returnType.IndexOf('<');
                returnType = returnType.Substring(bracketIndex + 1, returnType.Length - bracketIndex - 2);
            }

            // For static extension methods, skip the 'this' parameter
            var parameters = method.Modifiers
                .Any(m => m.IsKind(SyntaxKind.StaticKeyword))
                ? method.ParameterList.Parameters
                    .Where(p => !p.Modifiers
                        .Any(m => m.IsKind(SyntaxKind.ThisKeyword)))
                    .ToDictionary(p => p.Identifier.Text,
                        p => p.Type!.ToString())
                : method.ParameterList.Parameters
                    .ToDictionary(p => p.Identifier.Text,
                        p => p.Type!.ToString());

            SerializableMethodRecord record = new(typeSyntax.Identifier.Text,
                method.Identifier.Text,
                parameters,
                returnType);

            methodRecords.Add(record);
        }

        return methodRecords;
    }

    private const string SerializedMethodAttributeName = "SerializedMethod";
    private const string ProtoSerializableAttribute = "ProtobufSerializable";
}

/// <summary>
///     Represents metadata about a method marked for serialization, including its class, name, parameters, and return
///     type.
/// </summary>
/// <param name="ClassName">The name of the class containing the method.</param>
/// <param name="MethodName">The name of the method.</param>
/// <param name="Parameters">A dictionary mapping parameter names to their type names.</param>
/// <param name="ReturnType">The return type of the method, or null if void.</param>
public record SerializableMethodRecord(
    string ClassName,
    string MethodName,
    Dictionary<string, string> Parameters,
    string ReturnType)
{
    /// <summary>
    ///     The name of the class containing the method.
    /// </summary>
    public string ClassName { get; } = ClassName;

    /// <summary>
    ///     The name of the method.
    /// </summary>
    public string MethodName { get; } = MethodName;

    /// <summary>
    ///     A dictionary mapping parameter names to their type names.
    /// </summary>
    public Dictionary<string, string> Parameters { get; } = Parameters;

    /// <summary>
    ///     The return type of the method, or null if void.
    /// </summary>
    public string? ReturnType { get; } = ReturnType;
}